"use strict";

//・演算子
//さまざまな数値計算をするための演算子について見ていきます。
console.log(10 + 3); // 13
console.log(10 - 3); // 7
console/log(10 * 3); // 30
console.log(10 ** 3); // 1000
console.log(10 / 3); // 3.3333....
console.log(10 % 3); // 1

console.log(10 + 2 * 3); // 16
console.log((10 + 2) * 3); // 36


//・定数
// 数値に名前をつけて管理することができる定数について見ていきます。
const price = 150;
const rate = 1.1;

console.log(price * 120 * rate);
console.log(price * 130 * rate);
console.log(price * 140 * rate);

